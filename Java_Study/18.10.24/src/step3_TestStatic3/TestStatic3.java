package step3_TestStatic3;

public class TestStatic3 {

	public static void main(String[] args) {
		// API static method 활용
		String s1 = "1";
		String s2 = "2";
		int i = Integer.parseInt(s1);
		int j = Integer.parseInt(s2);
		System.out.println(s1 + s2);	// 문자 합산
		System.out.println(i + j);		// int 합산
	}
}